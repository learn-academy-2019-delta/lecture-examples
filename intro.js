// JavaScript – lightweight programming language (small memory footprint, doesn’t take up a lot of server energy), dynamic, loosely typed (don’t have to declare the data type and can change a variable to a different type without issue),  (vs. static/strong where a data type is declared for each variable upon declaration like C, Java), uses camelCase, JS is an interpreted (line by line) language (vs. complied which processes the source code to machine code)

